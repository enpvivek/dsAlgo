#include <bits/stdc++.h>
using namespace std;

void passTheExam(int A, int B, int C) {
  if (A + B + C < 100) {
    cout << "FAIL" << endl;
    return;
  }
  if (A >= 10 && B >= 10 && C >= 10) {
    cout << "PASS" << endl;
  }
  else {
    cout << "FAIL" << endl;
  }

}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int A, B, C;
    cin >> A >> B >> C;
    passTheExam(A, B, C);
  }

  return 0;
}