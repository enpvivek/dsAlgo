#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define int long long
#define endl "\n"



signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {
        string s;
        cin >> s;
        int balance = 0;
        int maxBalance = 0;
        for (int i = 0; i < s.length(); i++) {
            if (s[i] == '(') {
                balance += 1;
            }
            if (s[i] == ')') {
                balance -= 1;
            }
            maxBalance = max(balance, maxBalance);
        }
        //cout << maxBalance << endl;
        for (int i = 0; i < maxBalance; i++) {
            cout << '(';
        }
        for (int i = 0; i < maxBalance; i++) {
            cout << ')';
        }
        cout << endl;
    }


    return 0;
}





