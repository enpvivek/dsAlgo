#include <bits/stdc++.h>
using namespace std;

void gameOfPiles(int arr[], int n){
  int sum= 0;
  for(int i = 0; i < n; i++){
    if(arr[i]==1){
      cout<<"CHEF"<<endl;
      return;
    }
    else{
      sum+=arr[i];
    }
  }
  if(sum%2==0){
    cout<<"CHEFINA"<<endl;
  }
  else{
    cout<<"CHEF"<<endl;
  }
}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    int arr[n];
    for(int i = 0; i < n; i++){
      cin>>arr[i];
    }
    gameOfPiles(arr, n);
  }

  return 0;
}