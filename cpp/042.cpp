#include <bits/stdc++.h>
using namespace std;

#define SPEED ios::sync_with_stdio(0), cin.tie(0);
#define int long long
#define endl "\n"


int32_t main() {
  SPEED;
  int t;
  cin >> t;
  while (t--) {
    int size = 0;
    int N, B;
    cin >> N >> B;
    while (N--) {
      int W, H, P;
      cin >> W >> H >> P;
      if (P <= B) {
        size = max(size, W * H);
      }
    }
    if (size == 0) {
      cout << "no tablet" << endl;
    }
    else {
      cout << size << endl;
    }
  }

  return 0;
}

