void chefDiet(int arr[], int n, int k) {
  int store = 0;
  for (int i = 0; i < n; i++) {
    if (arr[i] + store - k < 0) {
      cout << "NO" << " " << i + 1 << endl;
      return;
    }
    else {
      store = arr[i] + store - k;
      //cout << store << endl;
    }
  }
  cout << "YES" << endl;
}

