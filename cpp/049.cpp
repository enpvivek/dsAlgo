#define SPEED ios::sync_with_stdio(0), cin.tie(0);
#define int long long
#define endl "\n"


void validMinimum(int  A, int B, int C) {
  int a, b, c;
  a = max(A, max(B, C));
  c = min(A, min(B, C));
  b = A + B + C - a - c;
  if (a >= b && b > c) {
    cout << "NO" << endl;
  }
  else {
    cout << "YES" << endl;
  }


}

