#include <bits/stdc++.h>
using namespace std;

void snakeProcession(string s, int n) {
  int res = 0;
  int i = 0;
  while (i < n) {
    if (res > 1 || res < 0) {
      cout << "Invalid" << endl;
      return;
    }
    if (s[i] == '.') {
      i++;
    }
    if (s[i] == 'H') {
      res += 1;
      i++;
    }
    else if (s[i] == 'T') {
      res -= 1;
      i++;
    }

    //cout << "hi";
  }
  if (res == 0) {
    cout << "Valid" << endl;
  }
  else {
    cout << "Invalid" << endl;
  }
}

int main() {
  int T;
  cin >> T;
  while (T--) {
    int n;
    cin >> n;
    string s;
    cin >> s;

    snakeProcession(s, n);
  }

  return 0;
}
