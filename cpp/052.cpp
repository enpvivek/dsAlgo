#include <bits/stdc++.h>
using namespace std;

#define SPEED ios::sync_with_stdio(0), cin.tie(0);
//#define int long long
#define endl "\n"

int cuttingRecipes(int arr[], int n) {
  if (n == 1) {
    return 1;
  }
  if (n == 2) {
    return __gcd(arr[0], arr[1]);
  }
  int g = __gcd(arr[0], arr[1]);
  for (int i = 2; i < n; i++) {
    g = __gcd(g, arr[i]);
  }
  return g;

}

int32_t main() {
  SPEED;
  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++) cin >> arr[i];
    for (int i = 0; i < n; i++) {
      cout << arr[i] / cuttingRecipes(arr, n) << " ";
    }
    cout << endl;
  }
  return 0;
}



