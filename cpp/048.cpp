#include <bits/stdc++.h>
using namespace std;

#define SPEED ios::sync_with_stdio(0), cin.tie(0);
#define int long long
#define endl "\n"


int nButtonForBulb(string s, string r, int n) {
  int cnt = 0;
  for (int i = 0; i < n; i++) {
    if (s[i] != r[i]) {
      cnt++;
    }
  }
  if (cnt % 2 == 0) {
    return 1;
  }
  else {
    return 0;
  }
}

int32_t main() {
  SPEED;
  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    string s, r;
    cin >> s >> r;
    cout << nButtonForBulb(s, r, n) << endl;
  }

  return 0;
}

