#include <bits/stdc++.h>
using namespace std;

void chefAndFeedback(string s1) {
  bool flag = false;
  for (int i = 1; i < s1.length() - 1; i++) {
    if ((s1[i] == '1' && s1[i - 1] == '0' && s1[i + 1] == '0') || (s1[i] == '0' && s1[i - 1] == '1' && s1[i + 1] == '1')) {
      flag = true;
      break;
    }
  }
  if (flag == true) {
    cout << "Good" << endl;
  }
  else {
    cout << "Bad" << endl;
  }

}


int main() {
  int t;
  cin >> t;
  while (t--) {
    string s;
    cin >> s;
    chefAndFeedback(s);
  }

  return 0;
}