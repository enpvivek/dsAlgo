#include <bits/stdc++.h>
using namespace std;

#define SPEED ios::sync_with_stdio(0), cin.tie(0);
#define int long long
#define endl "\n"

int weightBalance(int w1, int w2 , int x1, int x2, int m) {
  if (( float(w2 - w1) / float( m)) > x2 || (float(w2 - w1) / float( m)) < x1) {
    return 0;
  }
  else {
    return 1;
  }
}

int32_t main() {
  SPEED;
  int t;
  cin >> t;
  while (t--) {
    int w1, w2, x1, x2, m;
    cin >> w1 >> w2 >> x1 >> x2 >> m;
    cout << weightBalance(w1, w2, x1, x2, m) << endl;
  }

  return 0;
}

