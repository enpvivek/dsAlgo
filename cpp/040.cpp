void encodingMessage(string s, int n) {
  int n1 = n;
  if (n % 2 != 0) {
    n1 = n - 1;
  }
  for (int i = 0 ; i < n1; i += 2) {
    swap(s[i], s[i + 1]);
  }
  for (int i = 0; i < n; i++) {
    if (s[i] > 109 ) {
      s[i] = char(s[i] - (2 * (s[i] - 109)) + 1);
     // cout << (2 * (s[i] - 109)) << " ";
    }
    else {
      s[i] = char(109 + (109 - s[i]) + 1);
      //cout << (109 - s[i]) << " ";
    }
  }

  cout << s << endl;
}

