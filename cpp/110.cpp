#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define int long long
#define endl "\n"



signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {
        int n;
        cin >> n;
        int x = n - 1;
        int arr[n];
        for (int i = 0; i < (x + 1) / 2; i++) {
            arr[i] = n;
            arr[x - i] = n - 1;
            n -= 2;
            //cout << i << " " << x - i << endl;
        }
        if (x % 2 == 0) {
            arr[x / 2] = 1;
        }
        for (int i = 0; i <= x; i++) {
            cout << arr[i] << " ";
        }
        cout << endl;
    }



}


