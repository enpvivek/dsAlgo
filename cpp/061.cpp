#include <bits/stdc++.h>
using namespace std;

#define SPEED ios::sync_with_stdio(0), cin.tie(0);
#define int long long
#define endl "\n"

void chefAndDigit(string s) {
    int n = s.length();
    int count0 = 0;
    int count1 = 0;
    for (int i = 0; i < n; i++) {
        if (s[i] == '0') {
            count0++;
        }
        else if (s[i] == '1') {
            count1++;
        }
    }
    if (count1 == 1 || count0 == 1) {
        cout << "Yes" << endl;
    }
    else {
        cout << "No" << endl;
    }
}

int32_t main() {
    SPEED;
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;
        chefAndDigit(s);
    }
    return 0;


}




