#include <bits/stdc++.h>
using namespace std;

int largestFamily(int arr[], int n) {
  sort(arr, arr + n);
  int sum = 0;
  int i = 0;
  while (sum < n) {

    sum += arr[i];
    i++;
  }
  if (sum >= n) {
    return i - 1;
  }
  else {
    return i;
  }
  //return i - 1;

}

int main() {
  int T;
  cin >> T;
  while (T--) {
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++) {
      cin >> arr[i];
    }
    cout << largestFamily(arr, n) << endl;
  }
  return 0;
}
