#include <bits/stdc++.h>
using namespace std;

#define SPEED ios::sync_with_stdio(0), cin.tie(0);
#define int long long
#define endl "\n"

int brokenTelephone(int arr[], int n) {
    int count = 0;
    if (arr[0] != arr[1]) {
        count++;
    }
    if (arr[n - 1] != arr[n - 2]) {
        count++;
    }
    for (int i = 1; i < n - 1; i++) {
        if (arr[i] != arr[i - 1] || arr[i] != arr[i + 1]) {
            count++;
        }
    }
    return count;
}

int32_t main() {
    SPEED;
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        int arr[n];
        for (int i = 0; i < n; i++) {
            cin >> arr[i];
        }
        cout << brokenTelephone(arr, n) << endl;
    }
    return 0;
}




