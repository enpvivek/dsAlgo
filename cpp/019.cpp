#include <bits/stdc++.h>
using namespace std;

int sticksCutter(int arr[], int n) {
  set <int> s;
  for (int i = 0; i < n; i++) {
    if (arr[i] != 0) {
      s.insert(arr[i]);
    }
  }
  return s.size();
}

int main() {
  int T;
  cin >> T;
  while (T--) {
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++) {
      cin >> arr[i];
    }
    cout << sticksCutter(arr, n) << endl;
  }


  return 0;
}
