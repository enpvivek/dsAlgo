#include <bits/stdc++.h>
using namespace std;

void zeroOne(int n) {
  if (n == 3) {
    cout << "010" << endl;
  }
  else if (n == 4) {
    cout << "1001" << endl;
  }
  else if (n % 2 == 0) {
    cout << "10";
    for (int i = 0; i < n - 4; i++) {
      cout << "0";
    }
    cout << "01" << endl;
  }
  else if (n % 2 != 0) {
    for (int i = 0; i < n / 2; i++) {
      cout << "0";
    }
    cout << "1";
    for (int i = 0; i < n / 2; i++) {
      cout << "0";
    }
    cout << "\n";
  }
}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    zeroOne(n);
  }

  return 0;
}
