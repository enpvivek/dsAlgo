#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define int long long
#define endl "\n"

signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {
        int n, x;
        cin >> n >> x;
        int opt = 0;
        for (int i = 0; i < n; i++) {
            int s, r;
            cin >> s >> r;
            if (s <= x) {
                opt = max(opt, r);
            }
        }
        cout << opt << endl;
    }

    return 0;
}





