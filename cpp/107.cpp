#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define int long long
#define endl "\n"

void chefAndParty(int arr[], int n) {
    sort(arr, arr + n);
    for (int i = 0; i < n; i++) {
        if (i  < arr[i]) {
            cout << i << endl;
            return;
        }
    }
    cout << n << endl;
}

signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {
        int n;
        cin >> n ;
        int arr[n];
        for (int i = 0; i < n; i++) {
            cin >> arr[i];
        }
        chefAndParty(arr, n);
    }


    return 0;
}





