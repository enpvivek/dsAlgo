#include <bits/stdc++.h>
using namespace std;

void dannyWantsToKnow(unsigned long long int A[], unsigned long long int B[], int n, int l, int r) {
  unsigned long long int res = 0;
  for (int i = l - 1; i < r; i++) {
    res += A[i] * B[i];
  }
  cout<<res<<endl;
}

int main() {
  int n, q;
  cin >> n >> q;
  unsigned long long int A[n];
  unsigned long long int B[n];
  for (int i = 0; i < n; i++) {
    cin >> A[i];
  }
  for (int i = 0; i < n; i++) {
    cin >> B[i];
  }
  while (q--) {
    int l, r;
    cin >> l >> r;
    dannyWantsToKnow(A, B, n, l, r);
  }


  return 0;
}
