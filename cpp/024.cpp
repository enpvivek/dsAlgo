#include <bits/stdc++.h>
using namespace std;

void easyPronunciation(string s, int n) {
  int res = 0;
  int i = 0;
  //cout << res << endl;
  while (i <= n) {
    if (res == 4) {
      // cout << res << endl;
      cout << "NO" << endl;
      return;
    }


    if (s[i] == 'a' || s[i] == 'e' || s[i] == 'i' || s[i] == 'o' || s[i] == 'u') {
      res = 0;
      //cout << res << " " << s[i];
    }
    else if ((s[i] != 'a') || (s[i] != 'e') || (s[i] != 'i') || (s[i] != 'o' ) || (s[i] != 'u') ) {
      res++;
      //cout << res << " " << s[i];
    }

    i++;
  }

  cout << "YES" << endl;
}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    string s;
    cin >> s;
    easyPronunciation(s, n);
  }
}


