#include <bits/stdc++.h>
using namespace std;

//#define int long long int

int fillTheGrid(int n, int m) {
  if (n % 2 == 0 && m % 2 == 0) {
    return 0;
  }
  else if (n % 2 == 0 && m % 2 != 0) {
    return n;
  }
  else if (m % 2 == 0 && n % 2 != 0) {
    return m;
  }
  else {
    return m + n - 1;
  }
}


int main() {
  int t;
  cin >> t;
  while (t--) {
    int n, m;
    cin >> n >> m;
    cout << fillTheGrid(n, m) << endl;
  }

  return 0;
}
