#include <bits/stdc++.h>
using namespace std;

long long int divideOrNotDivide(int A, int B, int N) {
  if (A % B == 0 ) {
    return -1;
  }
  else {
    while (!(N % A == 0 && N % B != 0)) {
     N += (A - N % A);
    }

  }
  return N;
}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int A, B;
    long long int N;
    cin >> A >> B >> N ;

    cout << divideOrNotDivide(A, B, N) << endl;
  }

  return 0;
}

