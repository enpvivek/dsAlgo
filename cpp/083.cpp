int carvans(vector<int> v, int n) {
    int minM = INT_MAX;
    int count = 0;
    for (int i = 0; i < n; i++) {
        //maxM = max(v[i], maxM);
        if (v[i] <= minM) {
            count++;
            minM = v[i];
        }
    }
    return count;
}

