#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define int long long
#define endl "\n"



signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {
        int n, k;
        cin >> n >> k;
        int arr[n];
        for (int i = 0; i < n; i++) {
            cin >> arr[i];
        }
        int minM = INT_MAX;
        int maxM = -1;
        for (int i = 0; i < n; i++) {
            if (arr[i] > maxM) {
                maxM = arr[i];
            }
            if (arr[i] < minM) {
                minM = arr[i];
            }
        }
        if (n == 1) {
            cout << "YES" << endl;
        }
        else if (k >= minM + maxM) {
            cout << "YES" << endl;
        }
        else {
            cout << "NO" << endl;
        }
    }

    return 0;
}
