#include <bits/stdc++.h>
using namespace std;

int averageNumbers(int arr[], int n, int k, int v) {
  int sum = 0;
  for (int i = 0; i < n; i++) {
    sum += arr[i];
  }
  if (v * (n + k) - sum <= 0) {
    return -1;
  }
  else if ((v * (n + k) - sum) % k != 0) {
    return -1;
  }
  else {
    return (v * (n + k) - sum) / k;
  }
}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int n, k, v;
    cin >> n >> k >> v;
    int arr[n];
    for (int i = 0; i < n; i++) {
      cin >> arr[i];
    }
    cout << averageNumbers(arr, n, k, v) << endl;
  }

  return 0;
}
