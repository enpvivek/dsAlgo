#include <bits/stdc++.h>
using namespace std;

#define SPEED ios::sync_with_stdio(0), cin.tie(0);
//#define int long long
#define endl "\n"



int32_t main() {
  SPEED;
  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    int odd = 0;
    for (int i = 0; i < n; i++) {
      int x; cin >> x;
      if (x % 2 != 0) {
        odd++;
      }
    }
    if(n==1){
        cout<<1<<endl;
    }
    else if (odd % 2 == 0) {
      cout << 1 << endl;
    }
    else {
      cout << 2 << endl;
    }
  }
  return 0;
}



