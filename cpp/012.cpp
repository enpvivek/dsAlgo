#include <bits/stdc++.h>
using namespace std;

int endSorted(int arr[], int n) {
  int minP = 0;
  int maxP = 0;
  for (int i = 0; i < n; i++) {
    if (arr[i] == 1) {
      minP = i;
    }
    if (arr[i] == n) {
      maxP = i;
    }
  }
  //cout << minP << " " << maxP << endl;
  if (minP > maxP) {
    return (n - maxP - 2 + minP) ;
  }
  else {
    return (n - maxP - 1 + minP) ;
  }
}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++) {
      cin >> arr[i];
    }
    cout << endSorted(arr, n);
    cout << endl;
  }


  return 0;
}