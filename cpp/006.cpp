#include <bits/stdc++.h>
using namespace std;

void bidding(int A, int B, int C) {
  if (A > B && A > C) {
    cout << "ALICE" << endl;
  }
  else if (B > A && B > C) {
    cout << "BOB" << endl;
  }
  else if (C > A && C > B) {
    cout << "CHARLIE" << endl;
  }
}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int A, B, C;
    cin >> A >> B >> C;
    bidding(A, B, C);
  }

  return 0;
}