#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define int long long
#define endl "\n"

signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {
        int n, p, x, y;
        cin >> n >> p >> x >> y;
        int time = 0;
        int arr[n];
        for (int i = 0; i < n; i++) {
            cin >> arr[i];
        }
        for (int i = 0; i < p; i++) {
            if (arr[i] == 0) {
                time += x;
            }
            else if (arr[i] == 1) {
                time += y;
            }
        }
        cout << time << endl;
    }

    return 0;
}
