#include <bits/stdc++.h>
using namespace std;

int chefAndCandies(int N, int X) {
  //cout << N << " " << X << " " << N - X << " " << ((N - X) % 4);
  if (X > N) {
    return 0;
  }
  else {
    if ((N - X) % 4 == 0) {
      return ((N - X) / 4);
    }
    else {
      return ((N - X) / 4) + 1;
    }
  }
}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int N, X;
    cin >> N >> X;
    cout << chefAndCandies(N, X) << endl;
  }

  return 0;
}