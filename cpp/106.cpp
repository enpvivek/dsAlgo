#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define int long long
#define endl "\n"



signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {
        int n, k;
        cin >> n >> k;
        int arr[n];
        for (int i = 0; i < n; i++) {
            cin >> arr[i];
        }
        sort(arr, arr + n);
        double sum = 0.0;
        for (int i = k; i < n - k; i++) {
            //cout << arr[i] << " ";
            sum += arr[i];
        }
        //cout << sum << endl;
        int l = n - 2 * k;
        double d = sum / l;
        cout << fixed << setprecision(6) << d << endl;
    }


    return 0;
}





