#include <bits/stdc++.h>
using namespace std;

#define SPEED ios::sync_with_stdio(0), cin.tie(0);
#define int long long
#define endl "\n"

void minAttendanceReq(string s, int n) {
  int count = 0;
  for (int i = 0; i < n; i++) {
    if (s[i] == '1') {
      count++;
    }
  }
  //cout << count << endl;
  if (float(count + 120 - n) / float(120) >= 0.75) {
    cout << "YES" << endl;
  }
  else {
    cout << "NO" << endl;
  }
}

int32_t main() {
  SPEED;
  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    string s;
    cin >> s;
    minAttendanceReq(s, n);
  }
  return 0;
}



