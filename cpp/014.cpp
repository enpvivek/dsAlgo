#include <bits/stdc++.h>
using namespace std;

void ATMMachine(int arr[], int n, int k) {
  //int moneyWithdraw = 0;
  int moneyRemaining = k;
  for (int i = 0; i < n; i++) {
    if (moneyRemaining >= arr[i]) {
      cout << 1;
      moneyRemaining -= arr[i];
      // cout << moneyRemaining << endl;
    }
    else {
      cout << 0;
    }
  }
  cout << "\n";
}

int main() {
  int t;
  cin >> t;
  while (t--) {
    int n, k;
    cin >> n >> k;
    int arr[n];
    for (int i = 0; i < n ; i++) {
      cin >> arr[i];
    }
    ATMMachine(arr, n , k);
  }

  return 0;
}
