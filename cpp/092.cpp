#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define int long long
#define endl "\n"

void covidSpread(int arr[], int n) {
    vector<int> v;
    int sum = 0;
    for (int i = 0; i < n - 1; i++) {
        sum += 1;
        if (arr[i + 1] - arr[i] > 2) {
            v.push_back(sum);
            sum = 0;
        }
    }
    sum += 1;
    v.push_back(sum);
    int minm = 11;
    int maxm = 0;
    for (auto it : v) {
        if (it < minm) {
            minm = it;
        }
        if (it > maxm) {
            maxm = it;
        }
    }
    cout << minm << " " << maxm << endl;
}

signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {
        int n;
        cin >> n;
        int arr[n];
        for (int i = 0; i < n; i++) {
            cin >> arr[i];
        }
        covidSpread(arr, n);
    }
    return 0;
}