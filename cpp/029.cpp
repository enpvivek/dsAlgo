#include <bits/stdc++.h>
using namespace std;

//#define int long long int

int fillCandies(int n, int k, int m) {
	int cap = k * m;
	if (n % cap == 0) {
		return n / cap;
	}
	else {
		return ( n / cap) + 1;
	}
}


int main() {
	int t;
	cin >> t;
	while (t--) {
		int n, k, m;
		cin >> n >> k >> m;
		cout << fillCandies(n, k, m) << endl;
	}
	//cout << 25 / 8;
	return 0;
}
