#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
//#define int long long
#define endl "\n"


signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {

        int n;
        cin >> n;
        int arr[n];
        int a, b, s;

        a = 0; b = 0; s = 0;
        for (int i = 1; i <= n; ++i) {
            cin >> arr[i];
            a += arr[i] == 0;
            b += arr[i] == 1;
        }
        for (int i = 1; i <= n; ++i) {
            if (i == 1) s += (arr[1] == arr[n]);
            else s += arr[i] == arr[i - 1];
        }
        s -= abs(a - b);
        if (s % 4 == 0) {
            cout << "Bob" << endl;

        }
        else {
            cout << "Alice" << endl;
        }

    }
    return 0;
}