#include <bits/stdc++.h>
using namespace std;
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
//#define int long long
#define endl "\n"

void makeEqual(int a, int b) {
    if (a == b) {
        cout << "YES" << endl;
        return;
    }
    int maxM = max(a, b);
    int minM = min(a, b);
    while (maxM >= minM) {
        if (maxM % 2 == 0) {
            maxM = maxM / 2;
            //cout << maxM << endl;
            if (maxM == minM) {
                cout << "YES" << endl;
                return;
            }
        }
        else {
            break;
        }

    }
    cout << "NO" << endl;
}

signed main() {
    fastio;
    int tc;
    cin >> tc;
    while (tc--) {
        int a, b;
        cin >> a >> b;
        //cout << a << b << endl;
        makeEqual(a, b);

    }
    return 0;
}